int	ft_atoi(const char *str)
{
	int	i;
	int	nb;
	int	neg;

	nb = 0;
	neg = -1;
	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '+')
		i++;
	else if (str[i] == '-')
	{
		neg = 1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		nb = nb * 10 - str[i] + 48;
		i++;
	}
	nb *= neg;
	return (nb);
}
