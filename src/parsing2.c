int	no_other(char **tab)
{
	int	i;

	while (*tab)
	{
		i = 0;
		while ((*tab)[i])
		{
			if (!ft_strchr("01PEC", (*tab)[i]))
				return (1);
			i++;
		}
		tab++;
	}
	return (0);
}
